package com.zhenqi.entity.single.many2one;

/**
 *此类是多对一关联关系中的一方
 */
public class Many {
	private int id;
	private String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
